package src.com.shild.practice_theoretics7;

public class Example56SwitchString {
    public static void main (String [] args) {

   //Использовать символьные строки
        // для управления оператором Example56SwitchString
    String str = "two";
    switch (str) {
        case "one":
            System.out.println("one");
            break;
        case "two":
            System.out.println("two");
            break;
        case "three":
            System.out.println("three");
            break;
        default:
            System.out.println("did not match");
            break;
         }


    }
}
